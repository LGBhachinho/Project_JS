let nbre=7;

for (let pas = 1; pas <= 10; pas++) {
    // Ceci sera exécuté 5 fois
    // À chaque éxécution, la variable "pas" augmentera de 1
    // Lorsque'elle sera arrivée à 5, le boucle se terminera.
   
    console.log(nbre +" x " + pas +" = " + nbre * pas);
    console.log(`${nbre}  x  ${pas} = ${nbre * pas}`);
}